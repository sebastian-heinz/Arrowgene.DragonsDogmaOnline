    public class LightQuestRotationMigration : IMigrationStrategy
    {
        private static readonly ILogger Logger = LogProvider.Logger<Logger>(typeof(LightQuestRotationMigration));

        public uint From => 43;
        public uint To => 44;

        private readonly DatabaseSetting DatabaseSetting;

        public LightQuestRotationMigration(DatabaseSetting databaseSetting)
        {
            DatabaseSetting = databaseSetting;
        }

        public bool Migrate(IDatabase db, DbConnection conn)
        {
            string adaptedSchema = DdonDatabaseBuilder.GetAdaptedSchema(DatabaseSetting, "Script/migration_light_quest_refactor.sql");
            db.Execute(conn, adaptedSchema, true);

            List<QuestProgress> values = [];
            db.ExecuteReader(conn, "SELECT * FROM ddon_quest_progress;",
                command => {}, 
                reader =>
                {
                    while (reader.Read())
                    {
                        QuestProgress obj = new();
                        obj.CharacterCommonId = db.GetUInt32(reader, "character_common_id");
                        obj.QuestScheduleId = db.GetUInt32(reader, "quest_schedule_id");
                        obj.QuestType = (QuestType)db.GetUInt32(reader, "quest_type");
                        obj.Step = db.GetUInt32(reader, "step");
                        values.Add(obj);
                    }
                }
            );

            foreach(var progress in values)
            {
                uint questId = progress.QuestScheduleId;

                if (!QuestMapping.TryGetValue(questId, out var newSchedule))
                {
                    Logger.Error($"Failing to migrate quest progress {questId}, step {progress.Step} for commonID {progress.CharacterCommonId}");
                    continue;
                }

                uint newId = QuestScheduleId.GenerateScheduleId(newSchedule.Type, newSchedule.Group, newSchedule.Subgroup, newSchedule.Index, 0);
                db.ExecuteNonQuery(conn,
                    "UPDATE \"ddon_quest_progress\" SET \"quest_schedule_id\"=@new_schedule_id WHERE \"character_common_id\"=@character_common_id AND \"quest_schedule_id\"=@quest_schedule_id;",
                    command =>
                {
                    db.AddParameter(command, "character_common_id", progress.CharacterCommonId);
                    db.AddParameter(command, "quest_schedule_id", progress.QuestScheduleId);
                    db.AddParameter(command, "new_schedule_id", newId);
                });
            }

            
            List<QuestBoxRewards> rewards = [];
            db.ExecuteReader(conn, $"SELECT * FROM \"ddon_reward_box\";",
                command => {}, reader =>
                {
                    while (reader.Read())
                    {
                        QuestBoxRewards obj = new();
                        obj.UniqRewardId = db.GetUInt32(reader, "uniq_reward_id");
                        obj.CharacterCommonId = db.GetUInt32(reader, "character_common_id");
                        obj.NumRandomRewards = db.GetInt32(reader, "num_random_rewards");
                        obj.QuestScheduleId = db.GetUInt32(reader, "quest_schedule_id");

                        for (int i = 0; i < obj.NumRandomRewards; i++)
                        {
                            obj.RandomRewardIndices.Add(db.GetInt32(reader, $"random_reward{i}_index"));
                        }
                        rewards.Add(obj);
                    }
                }
            );

            db.ExecuteNonQuery(conn, "DELETE FROM \"ddon_reward_box\";", command => { });

            foreach (var reward in rewards)
            {
                uint questId = reward.QuestScheduleId;

                if (!QuestMapping.TryGetValue(questId, out var newSchedule))
                {
                    Logger.Error($"Failing to migrate quest reward {questId}, rewardID {reward.UniqRewardId} for commonID {reward.CharacterCommonId}");
                    continue;
                }

                uint newId = QuestScheduleId.GenerateScheduleId(newSchedule.Type, newSchedule.Group, newSchedule.Subgroup, newSchedule.Index, 0);

                db.ExecuteNonQuery(conn,
                    """
                    INSERT INTO "ddon_reward_box" (
                        "uniq_reward_id", 
                        "character_common_id", 
                        "quest_schedule_id", 
                        "num_random_rewards", 
                        "random_reward0_index", 
                        "random_reward1_index", 
                        "random_reward2_index", 
                        "random_reward3_index"
                    ) VALUES (
                        @uniq_reward_id, 
                        @character_common_id, 
                        @quest_schedule_id, 
                        @num_random_rewards, 
                        @random_reward0_index, 
                        @random_reward1_index, 
                        @random_reward2_index,
                        @random_reward3_index
                    );
                    """,
                    command =>
                    {
                        db.AddParameter(command, "@uniq_reward_id", reward.UniqRewardId);
                        db.AddParameter(command, "@character_common_id", reward.CharacterCommonId);
                        db.AddParameter(command, "@quest_schedule_id", newId);
                        db.AddParameter(command, "@num_random_rewards", reward.NumRandomRewards);
                        db.AddParameter(command, "@random_reward0_index", reward.RandomRewardIndices.ElementAtOrDefault(0));
                        db.AddParameter(command, "@random_reward1_index", reward.RandomRewardIndices.ElementAtOrDefault(1));
                        db.AddParameter(command, "@random_reward2_index", reward.RandomRewardIndices.ElementAtOrDefault(2));
                        db.AddParameter(command, "@random_reward3_index", reward.RandomRewardIndices.ElementAtOrDefault(3));
                    }
                );
            }

            return true;
        }


